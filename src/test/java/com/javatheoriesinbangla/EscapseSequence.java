package com.javatheoriesinbangla;

public class EscapseSequence {

	/*
	 * \b - backspace 
	 * \t - tab 
	 * \n - new line 
	 * \" - carriage return 
	 * \' - single quote
	 * \\ - backslash
	 */

	public static void main(String[] args) {

		System.out.println("Syed Abdullah");
		System.out.println("347-294-1234");
		System.out.println("BA in Finance");
		System.out.println();
		System.out.println("Syed Abdullah\n347-294-1234\nBA in Finance");

		System.out.println("1 \t 2");
		System.out.println("3\t 4");
		
		System.out.println(" Java Programming ");
		System.out.println(" \"Java Programming\" ");

	}

}
